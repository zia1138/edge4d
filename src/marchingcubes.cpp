  void MarchingCubes(float isolevel, volume8 &v, vector<vec3> &vtable, vector<face> &ftable, int c) {
    vtable.clear(); ftable.clear();
    hash_table vhash(1 << 16);  // Hash of pairs of integer positions of verticies.
    // Used to avoid duplicate vertex creation and face table construction.
    // March the center of the cube.
    for(int z = 0; z < v.depth - c; z += c) {
      for(int y = 0; y < v.height - c; y += c) {
	for(int x = 0; x < v.width - c; x += c) {
	  // Create vertex points of the cube.
	  ivec3 p0(x, y, z);                ivec3 p1(x, y + c, z);
	  ivec3 p2(x + c, y + c, z);        ivec3 p3(x + c, y, z);
	  ivec3 p4(x, y, z + c);            ivec3 p5(x, y + c, z + c);
	  ivec3 p6(x + c, y + c, z + c);    ivec3 p7(x + c, y, z + c);

	  // Sample the volume at those points. 
	  uint8 v0 = v(x, y, z);             uint8 v1 = v(x, y + c, z); 
	  uint8 v2 = v(x + c, y + c, z);     uint8 v3 = v(x + c, y, z);
	  uint8 v4 = v(x, y, z + c);         uint8 v5 = v(x, y + c, z + c);
	  uint8 v6 = v(x + c, y + c, z + c); uint8 v7 = v(x + c, y, z + c);

	  // Determine which verticies are inside the surface.
	  unsigned int cubeindex = 0;
	  if( v0 < isolevel ) cubeindex |= 1;   // Vertex 0
	  if( v1 < isolevel ) cubeindex |= 2;   // Vertex 1
	  if( v2 < isolevel ) cubeindex |= 4;   // Vertex 2
	  if( v3 < isolevel ) cubeindex |= 8;   // Vertex 3
	  if( v4 < isolevel ) cubeindex |= 16;  // Vertex 4
	  if( v5 < isolevel ) cubeindex |= 32;  // Vertex 5
	  if( v6 < isolevel ) cubeindex |= 64;  // Vertex 6
	  if( v7 < isolevel ) cubeindex |= 128; // Vertex 7

	  if(edgeTable[cubeindex] == 0) continue; // Cube is entirely in/out of the surface 

	  // (x,y,z) is an isosurface position it has a given cubeindex
	  int vl[12];
	  if(edgeTable[cubeindex] & 1)    vl[0]  = get_vid(vhash, vtable, isolevel, v0, v1, p0, p1); // Edge 0
	  if(edgeTable[cubeindex] & 2)    vl[1]  = get_vid(vhash, vtable, isolevel, v1, v2, p1, p2); // Edge 1
	  if(edgeTable[cubeindex] & 4)    vl[2]  = get_vid(vhash, vtable, isolevel, v2, v3, p2, p3); // Edge 2
	  if(edgeTable[cubeindex] & 8)    vl[3]  = get_vid(vhash, vtable, isolevel, v3, v0, p3, p0); // Edge 3
	  if(edgeTable[cubeindex] & 16)   vl[4]  = get_vid(vhash, vtable, isolevel, v4, v5, p4, p5); // Edge 4
	  if(edgeTable[cubeindex] & 32)   vl[5]  = get_vid(vhash, vtable, isolevel, v5, v6, p5, p6); // Edge 5 
	  if(edgeTable[cubeindex] & 64)   vl[6]  = get_vid(vhash, vtable, isolevel, v6, v7, p6, p7); // Edge 6
	  if(edgeTable[cubeindex] & 128)  vl[7]  = get_vid(vhash, vtable, isolevel, v7, v4, p7, p4); // Edge 7
	  if(edgeTable[cubeindex] & 256)  vl[8]  = get_vid(vhash, vtable, isolevel, v0, v4, p0, p4); // Edge 8
	  if(edgeTable[cubeindex] & 512)  vl[9]  = get_vid(vhash, vtable, isolevel, v1, v5, p1, p5); // Edge 9
	  if(edgeTable[cubeindex] & 1024) vl[10] = get_vid(vhash, vtable, isolevel, v2, v6, p2, p6); // Edge 10
	  if(edgeTable[cubeindex] & 2048) vl[11] = get_vid(vhash, vtable, isolevel, v3, v7, p3, p7); // Edge 11
	  // vl stores the vertex indicies for the given pair of points.

	  for(int i = 0; triTable[cubeindex][i] != -1; i+= 3) {
	    face F;
	    F.vert[0] = vl[triTable[cubeindex][i]];  
	    F.vert[1] = vl[triTable[cubeindex][i+1]];  
	    F.vert[2] = vl[triTable[cubeindex][i+2]];
	    ftable.push_back(F);
	  }
	}
      } 
    }
  }
