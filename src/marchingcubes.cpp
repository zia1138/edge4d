  // (px,py) -> interpolated point
  // 32-bytes?
  struct entry { 
    entry() { is_empty = true; }
    ivec3 px, py; // key coordinate pair sampled from voxel volume
    int vidx; // index of vertex in vertex table
    bool is_empty;
  };

  // Adapted from http://www.partow.net/programming/hashfunctions.
  inline unsigned int ELFHash(unsigned char* ptr, unsigned int len)  {
    unsigned int hash = 0, x = 0;
    for(unsigned int i = 0; i < len; ptr++, i++) {
      hash = (hash << 4) + (*ptr);
      if((x = hash & 0xF0000000) != 0) hash ^= (x >> 23);
      hash &= ~x;
    }
    return hash;
  }

  struct hash_table {
    vector<entry> data;
    unsigned int N; // number of items in hash table
    hash_table(unsigned int Nmax) { data.resize(2*Nmax); N = 0; }

    // Compute hash using cube vertices as keys.
    unsigned int hash(const ivec3 &px, const ivec3 &py) {
      unsigned char *px_data = (unsigned char*)&px;
      unsigned char *py_data = (unsigned char*)&py;
      // Place vertex points in a data buffer.
      unsigned char buf[2 * sizeof(ivec3)];
      for(unsigned int i = 0; i < sizeof(ivec3); i++) buf[i] = px_data[i];
      for(unsigned int i = 0; i < sizeof(ivec3); i++) buf[i+sizeof(ivec3)] = py_data[i];
      // Hash this buffer modoulo N. 
      return ELFHash(buf, 2*sizeof(ivec3)) % data.size();
    }
    
    // Re-hash.
    void expand() {
      vector<entry> tmp = data; 
      data.resize(tmp.size() * 2); // Resize data vector.
      for(size_t i = 0; i < data.size(); i++) 
	data[i].is_empty = true;
      N = 0;
      for(size_t i = 0; i < tmp.size(); i++)  // Re-hash data.
	if(!tmp[i].is_empty) insert(tmp[i].px, tmp[i].py, tmp[i].vidx);
      cout << "data.size()=" << data.size() << endl;
    }
    // Insert into hash table expanding as needed.
    void insert(const ivec3 px, const ivec3 py, int vidx) {
      unsigned int i = hash(px,py);
      while(!data[i].is_empty) i = (i+1) % data.size();
      data[i].px = px; 
      data[i].py = py; 
      data[i].vidx = vidx;
      data[i].is_empty = false;
      if(N++ >= data.size() / 2) expand();
    }
    // Search hash table, return -1 if not found.
    int search(const ivec3 px, const ivec3 py) {
      unsigned int i = hash(px,py);
      while(!data[i].is_empty) {
	if(data[i].px == px && data[i].py == py) return data[i].vidx;
	else i = (i + 1) % data.size();
      }
      return -1;
    }
  };

  // Insert verticies as needed.
  int get_vid(hash_table &vhash, vector<vec3> &vtable, 
	      float isolevel, float v1, float v2, ivec3 &p1, ivec3 &p2) {
    int vidx = vhash.search(p1, p2);
    if(vidx < 0) { // need to add vertex
      vidx = (int)vtable.size();
      vhash.insert(p1, p2, vidx); vhash.insert(p2, p1, vidx);
      vec3 p1v = p1.to_vec3(), p2v = p2.to_vec3();
      float mu = (isolevel - v1) / (v2 - v1); // interpolate vertex and add to table
      vtable.push_back(p1v + mu * (p2v - p1v)); 
    }
    return vidx;
  }

